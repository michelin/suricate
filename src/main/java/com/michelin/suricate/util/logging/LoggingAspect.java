package com.michelin.suricate.util.logging;

import lombok.extern.slf4j.Slf4j;
import org.aspectj.lang.ProceedingJoinPoint;
import org.aspectj.lang.annotation.Around;
import org.aspectj.lang.annotation.Aspect;
import org.springframework.context.annotation.Profile;
import org.springframework.stereotype.Component;

/**
 * Aspect for logging execution time of methods.
 */
@Slf4j
@Aspect
@Profile("prod")
@Component
public class LoggingAspect {
    /**
     * Log execution time of methods.
     *
     * @param joinPoint The join point
     * @return The object
     * @throws Throwable Exception thrown
     */
    @Around("@annotation(com.michelin.suricate.util.logging.LogExecutionTime)")
    public Object logExecutionTime(ProceedingJoinPoint joinPoint) throws Throwable {
        long start = System.currentTimeMillis();

        Object proceed = joinPoint.proceed();

        long executionTime = System.currentTimeMillis() - start;

        log.debug("{} executed in {} ms", joinPoint.getSignature(), executionTime);
        return proceed;
    }
}
